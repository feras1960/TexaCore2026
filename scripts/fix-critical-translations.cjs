// Process each language
Object.entries(criticalTranslations).forEach(([lang, translations]) => {
  const filePath = path.join(LOCALES_DIR, `${lang}.json`);
  let content = fs.readFileSync(filePath, 'utf8');
  
  const prefix = `[${lang.toUpperCase()}] `;
  let fixedCount = 0;
  
  Object.entries(translations).forEach(([key, value]) => {
    // Build regex that handles various formats
    const escapedKey = key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const regex = new RegExp(`"\\[${lang.toUpperCase()}\\] ${escapedKey}"`, 'g');
    
    const before = content;
    content = content.replace(regex, `"${value}"`);
    if (before !== content) fixedCount++;
  });
  
  fs.writeFileSync(filePath, content, 'utf8');
  console.log(`✅ ${lang.toUpperCase()}: Fixed ${fixedCount} placeholders`);
});

console.log('\n✅ Critical translations fixed!');
