-- ═══════════════════════════════════════════════════════════════
-- 💰 إضافة حقل نسبة الضريبة على مستوى المادة
-- ═══════════════════════════════════════════════════════════════
-- 
-- المسار الذهبي للضريبة:
-- 1️⃣ هل المادة لها ضريبة خاصة (tax_rate ≠ NULL)? → استخدمها
-- 2️⃣ إذا NULL → إعدادات الشركة (company_accounting_settings.vat_rate)
-- 3️⃣ إذا لم يجد إعداد → 0%
-- 
-- NULL = "استخدم الافتراضي من إعدادات الشركة"
-- 0    = "هذه المادة معفاة من الضريبة"
-- 4    = "هذه المادة عليها 4% ضريبة خاصة"
-- ═══════════════════════════════════════════════════════════════

ALTER TABLE fabric_materials 
ADD COLUMN IF NOT EXISTS tax_rate DECIMAL(5,2) DEFAULT NULL;

COMMENT ON COLUMN fabric_materials.tax_rate IS 
'نسبة الضريبة الخاصة بالمادة. NULL = استخدم إعداد الشركة الافتراضي. 0 = معفاة. قيمة أخرى = نسبة خاصة';
