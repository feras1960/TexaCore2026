-- ═══════════════════════════════════════════════════════════════════════════

SELECT 
    tablename as "الجدول",
    '❌ RLS معطل' as "الحالة"
FROM pg_tables
WHERE schemaname = 'public'
    AND rowsecurity = false
ORDER BY tablename;
