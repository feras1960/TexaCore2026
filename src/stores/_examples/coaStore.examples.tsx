import React from 'react';
import { useChartOfAccounts } from '@/stores/coaStore';
import { useAuth } from '@/hooks/useAuth';

// ═══════════════════════════════════════════════════════════════
// Example 1: Chart of Accounts Page
// ═══════════════════════════════════════════════════════════════

export function ChartOfAccountsPage() {
    const { currentCompany } = useAuth();
    const {
        accounts,
        isLoading,
        error,
        searchAccounts
    } = useChartOfAccounts(currentCompany?.id);

    const [searchQuery, setSearchQuery] = React.useState('');

    // Accounts are loaded automatically from cache (instant!)
    // and updated in realtime via subscriptions

    const filteredAccounts = searchQuery
        ? searchAccounts(searchQuery)
        : accounts;

    if (isLoading) {
        return <div>Loading...</div>;
    }

    if (error) {
        return <div>Error: {error}</div>;
    }

    return (
        <div>
            <h1>Chart of Accounts</h1>

            <input
                type="text"
                placeholder="Search accounts..."
                value={searchQuery}
                onChange={(e) => setSearchQuery(e.target.value)}
            />

            <div>
                {filteredAccounts.map(account => (
                    <div key={account.id}>
                        {account.account_code} - {account.name_ar}
                    </div>
                ))}
            </div>
        </div>
    );
}

// ═══════════════════════════════════════════════════════════════
// Example 2: Account Selector Component
// ═══════════════════════════════════════════════════════════════

interface AccountSelectorProps {
    value?: string;
    onChange: (accountId: string) => void;
    filterType?: 'cash' | 'bank' | 'receivable' | 'payable';
}

export function AccountSelector({ value, onChange, filterType }: AccountSelectorProps) {
    const { currentCompany } = useAuth();
    const {
        accounts,
        getCashAccounts,
        getBankAccounts,
        isLoading
    } = useChartOfAccounts(currentCompany?.id);

    // Filter accounts based on type
    const filteredAccounts = React.useMemo(() => {
        if (filterType === 'cash') return getCashAccounts();
        if (filterType === 'bank') return getBankAccounts();
        if (filterType === 'receivable') return accounts.filter(a => a.is_receivable);
        if (filterType === 'payable') return accounts.filter(a => a.is_payable);
        return accounts.filter(a => a.is_detail); // Only detail accounts
    }, [accounts, filterType]);

    if (isLoading) {
        return <select disabled><option>Loading...</option></select>;
    }

    return (
        <select value={value} onChange={(e) => onChange(e.target.value)}>
            <option value="">Select Account</option>
            {filteredAccounts.map(account => (
                <option key={account.id} value={account.id}>
                    {account.account_code} - {account.name_ar}
                </option>
            ))}
        </select>
    );
}

