// Example 3: Add Bank Account (Optimistic Update)
// ═══════════════════════════════════════════════════════════════

export function AddBankAccountForm() {
    const { currentCompany } = useAuth();
    const { addAccountOptimistic } = useChartOfAccounts(currentCompany?.id);
    const [isSubmitting, setIsSubmitting] = React.useState(false);

    const handleSubmit = async (e: React.FormEvent<HTMLFormElement>) => {
        e.preventDefault();
        setIsSubmitting(true);

        const formData = new FormData(e.currentTarget);

        try {
            // This will update UI instantly (optimistic)
            // and sync with server in background
            await addAccountOptimistic({
                company_id: currentCompany!.id,
                tenant_id: currentCompany!.tenant_id,
                account_code: '1126', // Will be auto-generated by function
                name_ar: formData.get('name_ar') as string,
                name_en: formData.get('name_en') as string,
                currency: formData.get('currency') as string,
                is_bank_account: true,
                is_detail: true,
                is_group: false,
            });

            // UI already updated! User sees instant feedback
            alert('Bank account added successfully!');

        } catch (error) {
            // Rollback happened automatically
            alert('Failed to add bank account');
        } finally {
            setIsSubmitting(false);
        }
    };

    return (
        <form onSubmit={handleSubmit}>
            <input name="name_ar" placeholder="Bank Name (Arabic)" required />
            <input name="name_en" placeholder="Bank Name (English)" required />
            <select name="currency" required>
                <option value="UAH">UAH</option>
                <option value="USD">USD</option>
                <option value="EUR">EUR</option>
            </select>
            <button type="submit" disabled={isSubmitting}>
                {isSubmitting ? 'Adding...' : 'Add Bank Account'}
            </button>
        </form>
    );
}

